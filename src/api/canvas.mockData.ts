function generateLectures(): CanvasCalendarEvent[] {
  const events: CanvasCalendarEvent[] = [];
  let id = 5000;

  // [courseId, title, dayOfWeek (0=Sun), startHour, endHour, room]
  const schedules: [number, string, number[], number, number, string][] = [
    [2001, "CS 301 Lecture",          [1, 3, 5], 9,  10, "Science Hall 204"],
    [2001, "CS 301 Office Hours",     [2],       14, 15, "Science Hall 212 (Prof. Huang)"],
    [2001, "CS 301 Office Hours",     [4],       14, 15, "Science Hall 212 (Prof. Huang)"],
    [2002, "MATH 220 Lecture",        [1, 3, 5], 11, 12, "Math Building 101"],
    [2002, "MATH 220 Office Hours",   [2],       10, 11, "Math Building 118 (Prof. Okonkwo)"],
    [2002, "MATH 220 Office Hours",   [5],       13, 14, "Math Building 118 (Prof. Okonkwo)"],
    [2003, "ENG 201",                 [2, 4],    13, 14, "Humanities 305"],
    [2003, "ENG 201 Office Hours",    [3],       11, 12, "Humanities 218 (Prof. Martinez)"],
    [2004, "PHYS 211 Lecture",        [1, 3],    10, 11, "Physics Hall 110"],
    [2004, "PHYS 211 Lab",            [3],       14, 17, "Physics Lab A"],
    [2004, "PHYS 211 Office Hours",   [2],       13, 14, "Physics Hall 104 (Prof. Kim)"],
    [2004, "PHYS 211 Office Hours",   [4],       13, 14, "Physics Hall 104 (Prof. Kim)"],
    [2005, "CS 350 Lecture",          [1, 3, 5], 14, 15, "Engineering 302"],
    [2005, "CS 350 Office Hours",     [2],       11, 12, "Engineering 308 (Prof. Patel)"],
    [2005, "CS 350 Office Hours",     [4],       11, 12, "Engineering 308 (Prof. Patel)"],
    [2006, "PSYC 101",                [2, 4],    11, 12, "Social Sciences 101"],
    [2006, "PSYC 101 Office Hours",   [1],       15, 16, "Social Sciences 215 (Prof. Nguyen)"],
  ];

  const semesterStart = new Date("2026-06-17");

  for (let week = 0; week < 9; week++) {
    for (const [courseId, title, days, startHour, endHour, room] of schedules) {
      for (const dow of days) {
        const day = new Date(semesterStart);
        day.setDate(semesterStart.getDate() + week * 7 + ((dow - semesterStart.getDay() + 7) % 7));

        // Don't generate past the semester end (~Aug 15)
        if (day > new Date("2026-08-15")) continue;

        const startAt = new Date(day);
        startAt.setHours(startHour, 0, 0, 0);
        const endAt = new Date(day);
        endAt.setHours(endHour, 0, 0, 0);

        events.push({
          id: id++,
          title,
          start_at: startAt.toISOString(),
          end_at: endAt.toISOString(),
          type: "event",
          workflow_state: "active",
          context_code: `course_${courseId}`,
          course_id: courseId,
          description: null,
          location_name: room,
          location_address: null,
        });
      }
    }
  }
  return events;
}

