// Helper — build an ISO date offset from June 17 2026
const T = (offsetDays: number, hour = 23, min = 59) => {
  const d = new Date("2026-06-17T00:00:00");
  d.setDate(d.getDate() + offsetDays);
  d.setHours(hour, min, 0, 0);
  return d.toISOString();
};

// Helper — build an ISO date from an absolute date string + time
const D = (date: string, hour = 23, min = 59) => {
  const d = new Date(`${date}T00:00:00`);
  d.setHours(hour, min, 0, 0);
  return d.toISOString();
};

